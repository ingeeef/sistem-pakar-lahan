<h1>Fuzzy Inference System Mamdani</h1>
<p>FIS (Fuzzy Inference System) Mamdani merupakan salah satu metode penalaran (inference) yang paling 
sering digunakan untuk persoalan kendali logika fuzzy. Kelebihan metode Mamdani dibandingkan metode 
sistem penalaran fuzzy yang lain, diantaranya adalah karena bersifat intuitif, mencakup bidang yang luas, 
dan sesuai dengan proses input informasi manusia. Sistem penalaran fuzzy metode Mamdani dikenal juga dengan 
nama metode Min - Max. Alasan kenapa sistem penalaran FIS Mamdani ini yang diterapkan, karena lebih menyerupai 
pola pikir manusia karena fungsi implikasi antara antecedent dengan consequent sama-sama dalam himpunan fuzzy 
Mamdani-FIS dalam melakukan komputasi untuk mendapatkan output yang diharapkan (Ahmadi., 2018)</p>